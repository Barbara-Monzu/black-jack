function SuffleDeck(deckCards){
    // shuffle the cards
    for (let i = deckCards.length - 1; i > 0; i--) {
      let j = Math.floor(Math.random() * i);
      let temp = deckCards[i];
      deckCards[i] = deckCards[j];
      deckCards[j] = temp;
    }
    console.log(deckCards);
}
